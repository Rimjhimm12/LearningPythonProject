######### Relational/Comparison Operators ###########
# >, <, >=, <=, !=, ==
# it returns boolean value

a, b = 10, 5
print(a>b) #true
print(a<b) #false
print(a>=b) #true
print(a<=b) #false
print(a==b) #false
print(a!=b) #true
print('__________________')
####### Logical Operator #######
# and or not
# it works between boolean data
# it returns boolean value
a = True
b = False
print(a and b) # false
print(a or b) #true
print(b or a) #true
print(not a) #false
print(not b) #true



